package network.structure;

public class Dimension {

	private float largeur;

	public Dimension(float l) {
		this.largeur = l;
	}

	public float getLargeur() {
		return largeur;
	}

	public void setLargeur(float largeur) {
		this.largeur = largeur;
	}

	public float val() {
		return this.largeur;
	}

}
